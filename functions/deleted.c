#include "../head.h"

void		deleted(char **map, char letter)
{
	int		y;
	int		x;

	y = 3;
	while (map[y])
	{
		x = 3;
		while (map[y][x])
		{
			if (map[y][x] == letter)
				map[y][x] = '.';
			x++;
		}
		y++;
	}
}
